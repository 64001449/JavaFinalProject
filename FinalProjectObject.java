package Classwork;

public class FinalProjectObject {
	private String eyeColor;
	private String hair;
	private String height;
	private String name;
	
	public FinalProjectObject(String e, String h, String he, String n) {
		eyeColor = e;
		hair = h;
		height = he;
		name = n;
	}
	public String getEye() {
		return eyeColor;
	}
	public String getHair() {
		return hair;
	}
	public String getHeight() {
		return height;
	}
	public String getName() {
		return name;
	}
}
